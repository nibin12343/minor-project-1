from ucimlrepo import fetch_ucirepo

# fetch dataset
breast_cancer_wisconsin_original = fetch_ucirepo(id=15)

# data (as pandas dataframes)
X = breast_cancer_wisconsin_original.data.features
y = breast_cancer_wisconsin_original.data.targets

# metadata
print(breast_cancer_wisconsin_original.metadata)

# variable information
print(breast_cancer_wisconsin_original.variables)

#Displaying the first 5 rows
X.head()

#Displaying the last 5 rows
X.tail()

#Displaying the description
X.describe().T

#Displaying the information on the type of data involved in the dataset
X.info()

#Displaying the information of the label in the dataset
y.info()

# Checking for missing values in the dataset
X.isnull().sum()

# 1. Handle missing values: 'Bare_nuclei' has 16 NULL values
# We'll use median imputation as the values are discrete (1-10)
# Make a copy of the data for calculation, that the original data is not corrupted data
X_clean = X.copy()
X_clean['Bare_nuclei'] = X_clean['Bare_nuclei'].fillna(X_clean['Bare_nuclei'].median())

#Import all the necessary packages
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
from sklearn.model_selection import train_test_split, GridSearchCV
from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import classification_report, confusion_matrix, roc_curve, auc

# Prepare target: The dataset uses 2 for benign and 4 for malignant.
# Mapping the value 0 means there is  non-cancerous, and 1 means cancerous

y_clean = y['Class'].map({2: 0, 4: 1})

# 2. Split the dataset
X_train, X_test, y_train, y_test = train_test_split(X_clean, y_clean, test_size=0.2, random_state=42, stratify=y_clean)

print(f"Training set size: {X_train.shape}")
print(f"Testing set size: {X_test.shape}")

# 3. Implement Random Forest and Initial Evaluation
rf_base = RandomForestClassifier(random_state=42)
rf_base.fit(X_train, y_train)

y_pred = rf_base.predict(X_test)

print("--- Initial Model Evaluation ---")
print(classification_report(y_test, y_pred))

# Confusion Matrix
plt.figure(figsize=(6,4))
sns.heatmap(confusion_matrix(y_test, y_pred), annot=True, fmt='d', cmap='Blues')
plt.title('Confusion Matrix - Base Model')
plt.xlabel('Predicted')
plt.ylabel('Actual')
plt.show()

# 4. Hyperparameter Tuning using GridSearchCV
param_grid = {
    'n_estimators': [50, 100, 200],
    'max_depth': [None, 5, 10],
    'min_samples_split': [2, 5],
    'max_features': ['sqrt', 'log2']
}

grid_search = GridSearchCV(RandomForestClassifier(random_state=42), param_grid, cv=5, scoring='f1')
grid_search.fit(X_train, y_train)

best_rf = grid_search.best_estimator_
print(f"Best Parameters: {grid_search.best_params_}")

# 5. Visualization: Feature Importance and ROC Curve

# Feature Importance
importances = best_rf.feature_importances_
indices = np.argsort(importances)

plt.figure(figsize=(10, 6))
plt.title('Feature Importances')
plt.barh(range(len(indices)), importances[indices], align='center')
plt.yticks(range(len(indices)), [X_clean.columns[i] for i in indices])
plt.xlabel('Relative Importance')
plt.show()

# ROC Curve
y_probs = best_rf.predict_proba(X_test)[:, 1]
fpr, tpr, _ = roc_curve(y_test, y_probs)
roc_auc = auc(fpr, tpr)

plt.figure(figsize=(8, 6))
plt.plot(fpr, tpr, color='darkorange', lw=2, label=f'ROC curve (area = {roc_auc:.2f})')
plt.plot([0, 1], [0, 1], color='navy', lw=2, linestyle='--')
plt.xlim([0.0, 1.0])
plt.ylim([0.0, 1.05])
plt.xlabel('False Positive Rate')
plt.ylabel('True Positive Rate')
plt.title('Receiver Operating Characteristic (ROC)')
plt.legend(loc="lower right")
plt.show()